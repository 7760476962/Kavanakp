package eg1;

public class RelationalDemo {
	public static void main(String[] args) {
		int a=7,b=11;
		System.out.println(" a is " +a+ " ans b is"+b);
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		
	}

}
